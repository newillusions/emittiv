		<h2>shaping spaces with sound</h2>
		<p>sound has a profound impact on how a space feels, yet it's often handled last. the playback system, the acoustics, the content, how people move — it's all connected. we treat it as one system.</p>

		<h2>beyond background music</h2>
		<p>sound design isn't just setting a playlist. it's about:</p>
		<ul>
			<li><strong>spatial audio</strong> that places sound in three dimensions</li>
			<li><strong>environmental sources</strong> that feel native to the space</li>
			<li><strong>purposeful content</strong> — curated or composed for the environment</li>
			<li><strong>zoning and control</strong> that gives each area its own character</li>
		</ul>

		<h2>networked audio systems</h2>
		<p>Dante, AES67 and modern networked platforms allow routing and reconfiguration with ease. zones shift in software. systems scale without redesign.</p>
		<p>we design audio that is:</p>
		<ul>
			<li><strong>flexible</strong> — ready for change</li>
			<li><strong>integrated</strong> — part of the wider sensory system</li>
			<li><strong>manageable</strong> — intuitive to operate</li>
		</ul>

		<h2>soundscapes and ambient audio</h2>
		<p>ambient soundscapes can define atmosphere subtly but powerfully: water and birdsong in wellness, the energy of a market in a food hall, quiet tonal layers in a lobby.</p>
		<p>we create bespoke audio content or collaborate with composers. when we bring composers in, it's early — understanding the system and the space shapes their work, and it shows in the end result.</p>

		<h2>through to handover</h2>
